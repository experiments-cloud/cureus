"""
Final Model Comparison: ResNet-50 vs. MobileNetV2
------------------------------------------------
This script executes the final comparative analysis under multiple 
visual degradation scenarios. It extracts key clinical metrics: 
Global Accuracy and Melanoma Sensitivity (Recall).

The output is formatted for direct inclusion in the Cureus manuscript.
"""

import torch
import torch.nn as nn
from torch.utils.data import DataLoader, random_split
import torchvision.models as models
from torchvision import transforms
import pandas as pd
from sklearn.metrics import classification_report

# Import custom dataset loader and degradation modules
from dataset_loader import HAM10000Dataset, data_transforms
from evaluacion_degradacion import AddGaussianNoise, DegradeBlur, DegradeBrightness

# ==========================================
# 1. Configuration & Pipelines
# ==========================================
DEVICE = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
CSV_PATH = 'data/HAM10000_metadata.csv'
IMAGES_DIR = 'data/images/'
BATCH_SIZE = 32
RANDOM_SEED = 42

# Define identical degradation pipelines as used in the study
degradation_pipelines = {
    'Baseline (Clean)': data_transforms,
    'Moderate_Blur': transforms.Compose([
        transforms.Resize((224, 224)),
        DegradeBlur(radius=2.5),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ]),
    'Low_Luminance': transforms.Compose([
        transforms.Resize((224, 224)),
        DegradeBrightness(factor=0.4),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ]),
    'Sensor_Noise': transforms.Compose([
         transforms.Resize((224, 224)),
         transforms.ToTensor(),
         AddGaussianNoise(0., 0.1),
         transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ])
}

# ==========================================
# 2. Model Loading Utility
# ==========================================
def load_trained_model(arch_name, path, device):
    """Initializes architecture and loads saved baseline weights."""
    if arch_name == 'resnet50':
        model = models.resnet50(weights=None)
        model.fc = nn.Linear(model.fc.in_features, 7)
    else: # mobilenet
        model = models.mobilenet_v2(weights=None)
        model.classifier[1] = nn.Linear(model.last_channel, 7)
        
    model.load_state_dict(torch.load(path, map_location=device))
    return model.to(device).eval()

# ==========================================
# 3. Evaluation Logic
# ==========================================
def evaluate_metrics(model, dataloader, device):
    """Extracts Global Accuracy and Melanoma Specific Sensitivity."""
    all_preds, all_labels = [], []
    with torch.no_grad():
        for inputs, labels in dataloader:
            inputs, labels = inputs.to(device), labels.to(device)
            outputs = model(inputs)
            _, preds = torch.max(outputs, 1)
            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())
            
    target_names = ['nv', 'mel', 'bkl', 'bcc', 'akiec', 'vasc', 'df']
    report = classification_report(all_labels, all_preds, target_names=target_names, 
                                   output_dict=True, zero_division=0)
    
    return report['accuracy'], report['mel']['recall']

if __name__ == '__main__':
    print(f"Executing Final Comparison on: {DEVICE}\n")

    # Load weights generated by model_training.py[cite: 1]
    models_to_test = {
        'ResNet-50': load_trained_model('resnet50', 'resnet50_baseline.pth', DEVICE),
        'MobileNetV2': load_trained_model('mobilenet', 'mobilenet_baseline.pth', DEVICE)
    }

    results = []
    
    # Calculate dataset split sizes once[cite: 1]
    temp_ds = HAM10000Dataset(CSV_PATH, IMAGES_DIR, transform=data_transforms)
    train_size = int(0.8 * len(temp_ds))
    test_size = len(temp_ds) - train_size

    for cond_name, transform in degradation_pipelines.items():
        print(f"Processing condition: {cond_name}...")
        
        # Ensure identical test set selection using RANDOM_SEED[cite: 1]
        eval_ds = HAM10000Dataset(CSV_PATH, IMAGES_DIR, transform=transform)
        generator = torch.Generator().manual_seed(RANDOM_SEED)
        _, test_ds = random_split(eval_ds, [train_size, test_size], generator=generator)
        loader = DataLoader(test_ds, batch_size=BATCH_SIZE, shuffle=False)
        
        for name, model in models_to_test.items():
            acc, mel_sens = evaluate_metrics(model, loader, DEVICE)
            results.append({
                'Model': name,
                'Condition': cond_name,
                'Global_Accuracy': f"{acc*100:.1f}%",
                'Melanoma_Sensitivity': f"{mel_sens*100:.1f}%"
            })

    # Final Table Generation[cite: 1]
    df_results = pd.DataFrame(results)
    print("\n" + "="*65)
    print("FINAL RESULTS TABLE FOR CUREUS MANUSCRIPT")
    print("="*65)
    print(df_results.to_string(index=False))
    df_results.to_csv('final_comparison_results.csv', index=False)
